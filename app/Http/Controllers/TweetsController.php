    public function getProfileTweets($handle)
    {
        $profile=UserProfile::where('url_handle',$handle)->first();

        $user=$profile->user;

        $tweets=Tweet::where('user_id',$user->id)->latest()->get();

        return view('users.profile',compact('tweets','user'));
    }

